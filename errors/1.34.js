// Пример
// плохо
    var someItem = 'some string';
    var anotherItem = 'another string';
    var oneMoreItem = 'one more string';
//хорошо
    var someItem = 'some string',
        anotherItem = 'another string',
        oneMoreItem = 'one more string';
